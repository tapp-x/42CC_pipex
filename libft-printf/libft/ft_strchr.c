#include "libft.h"

int	ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] != '\0' && (s[i] != c % 128))
		i++;
	if (s[i] == c % 128)
		return (i);
	return (0);
}
